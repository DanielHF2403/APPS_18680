package edu.iest.linearlayoutactivity

class Aritmetica {

    fun suma(num1: Float, num2:Float) : Float{
        return num1 + num2
    }

    fun resta(num1: Float, num2:Float) : Float{
        return num1 - num2
    }

    fun multiplicacion(num1: Float, num2:Float) : Float{
        return num1 * num2
    }

    fun division(num1: Float, num2:Float) : Float{
        return num1 / num2
    }
}